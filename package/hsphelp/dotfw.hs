ドットフレームワークの初期化

%inst
ドットフレームワークを初期化して使用可能な状態にします。
あらかじめ、df_config命令で必要な設定を行っておく必要があります。
df_reset命令は、画面の初期化を行いたい場合に、何度でも使用することができます。
画面の初期化を行うことで、スプライト、エフェクト等の情報がすべて初期状態に戻ります。



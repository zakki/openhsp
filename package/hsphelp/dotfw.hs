%inst
専用フォントメッセージエフェクトを追加します。
ドットフレームワークでは、通常のスプライトとは別にエフェクトと呼ばれる表示の仕組みが用意されています。
これはゲーム中のキャラクターとは関係なく画面に表示される効果などで使用することができます。
専用フォントメッセージエフェクトは、(p2,p3)の座標から1文字ずつ画面に文字を表示することができます。

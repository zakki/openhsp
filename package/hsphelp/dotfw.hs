%inst
背景に表示する星に関するパラメータを設定します。
星表示は、df_config命令で"star"のタグを指定することで有効になります。
df_star命令は、星表示が有効になった後でパラメータを変更する際に指定します。
星は、256×256ドットの絵として生成されます。これを表示する際のズーム値(倍率)や星を流す方向(回転スイッチ)などを制御して画面に合うよう調整することができます。

  bit 8 :  $100 スプライト表示ON
  bit 9 :  $200 スプライト移動ON
  bit10 :  $400 自由落下移動ON
  bit11 :  $800 BGとリンクして移動(未実装)
  bit12 : $1000 ボーダー消去無効ON
  bit13 : $2000 ボーダーXで反転SWITCH
  bit14 : $4000 ボーダーYで反転SWITCH
  bit15 : $8000 カウントダウンタイマー時点滅SWITCH





す。
p4は、0ならば透明色を透過し、1ならば不透明のキャラクタパターンになりま
す。
^
キャラクタのX,Yサイズには特に制限はありませんが、 オフスクリーンバッフ
ァからはみ出すようなキャラクタパターンの指定は無効になります。
